// find whether a number  is positive,negative or 0
package controlstatements;

public class number {

	public static void main(String[] args) {
		int x=0;
		if(x>0) {
			System.out.println("The number is positive");
		}
		else if(x<0){
			System.out.println("The number is negative");
			
		}
		else {
			System.out.println("The number is neither positive nor negative so it must be 0");
		}
		
	}

}
